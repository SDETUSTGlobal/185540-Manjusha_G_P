# accessing values in string
var1 = "Python!"
var2 = "Software Testing"
print("var1[0]:", var1[0])
print("var2[1:5]:", var2[0:8])
# replace
oldstring = 'I like Python'
newstring = oldstring.replace('like', 'love')  # like is replaced with love
print(newstring)

string = "python at PYTHON"
# uppercase lowercase
print(string.upper())
# capitalize
print(string.capitalize())
# lower
print(string.lower())
# join function
print(":".join("INDIA"))
# Reversing string
print(''.join(reversed(string)))
# Split Strings
print(string.split(' '))
